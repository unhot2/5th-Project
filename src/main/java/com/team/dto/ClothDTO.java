package com.team.dto;

public class ClothDTO {
	private int number;
	private String clothtitle;
	private String clothimgpath;
	private int clothprice;
	private int clothbuycount;
	private int clothremain;
	
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public String getClothtitle() {
		return clothtitle;
	}
	public void setClothtitle(String clothtitle) {
		this.clothtitle = clothtitle;
	}
	public String getClothimgpath() {
		return clothimgpath;
	}
	public void setClothimgpath(String clothimgpath) {
		this.clothimgpath = clothimgpath;
	}
	public int getClothprice() {
		return clothprice;
	}
	public void setClothprice(int clothprice) {
		this.clothprice = clothprice;
	}
	public int getClothbuycount() {
		return clothbuycount;
	}
	public void setClothbuycount(int clothbuycount) {
		this.clothbuycount = clothbuycount;
	}
	public int getClothremain() {
		return clothremain;
	}
	public void setClothremain(int clothremain) {
		this.clothremain = clothremain;
	}
	

		
}
